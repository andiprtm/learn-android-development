package anatomi_function

fun main() {
    /**
     * function anatomy
     *
     * - Function Header
     * private fun sum(number: Int, number2: Int): Int
     *
     * private = visibility modifier, if not declare the default set to public
     * fun sum = function name
     * (number: Int, number2: Int) = function parameter
     * : Int = function return type
     *
     * - Function Body
     * {
     *     return number + number2
     * }
     *
     *
     */
}

private fun sum(number: Int, number2: Int): Int{
    return number + number2
}