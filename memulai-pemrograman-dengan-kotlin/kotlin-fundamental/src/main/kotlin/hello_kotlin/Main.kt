package hello_kotlin

// main function -> this is a comment. the comment will not be compiled when the code is executed
fun main() {
    // to print some text use print() or println()
    // The difference is that print() will print an argument on the same line
    // and println() stands for print line will print the argument to the next line

    // print()
    print("Hello Kotlin!")
    print("Hello Andi!")
    // output : Hello Kotlin!Hello Andi!

    // println()
    println("Hello Kotlin!")
    println("Hello Andi")
    /*
    * multi-line comment
    * output:
    * Hello Kotlin!
    * Hello Andi!
    * */

}